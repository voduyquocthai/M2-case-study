    private void btnRealUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealUpdateActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblStudent.getSelectedRow();
        int currentId = -1;
        if(selectedIndex >= 0){
            Student std = studentList.get(selectedIndex);
            currentId = std.getId();
        }
        String fullname = txtFullName.getText();
        String gender = cbGender.getSelectedItem().toString();
        int age = Integer.parseInt(txtAge.getText());
        String email = txtEmail.getText();
        String phoneNumber = txtPhoneNumber.getText();
        
        Student newstd = new Student(currentId,fullname, gender, email, phoneNumber, age);
        
        StudentManagement.updateStudent(newstd);
        showStudent();
        if(currentId != -1){
            JOptionPane.showMessageDialog(this, "Succesfully Updated !");
        }
        
    }//GEN-LAST:event_btnRealUpdateActionPerformed

    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
        // TODO add your handling code here:
        int selectedIndex = tblStudent.getSelectedRow();
        txtFullName.setText(tableModel.getValueAt(selectedIndex, 1).toString());
        int option = -1;
        
        String gender = tableModel.getValueAt(selectedIndex, 2).toString();
        System.out.println(gender);
        if(gender.equals("Male")){
            option = 0;
        } else if(gender.equals("Female")){
            option = 1;
        }
            
        cbGender.setSelectedIndex(option);
        txtAge.setText(tableModel.getValueAt(selectedIndex, 5).toString());
        txtEmail.setText(tableModel.getValueAt(selectedIndex, 3).toString());
        txtPhoneNumber.setText(tableModel.getValueAt(selectedIndex, 4).toString());
    }//GEN-LAST:event_tblStudentMouseClicked

