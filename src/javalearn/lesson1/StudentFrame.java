    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String fullname = txtFullName.getText();
        String gender = cbGender.getSelectedItem().toString();
        int age = Integer.parseInt(txtAge.getText());
        String email = txtEmail.getText();
        String phoneNumber = txtPhoneNumber.getText();
        
        Student std = new Student(fullname, gender, email, phoneNumber, age);
        
        StudentManagement.insertStudent(std);
        
        showStudent();
        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblStudent.getSelectedRow();
        if(selectedIndex >= 0){
            Student std = studentList.get(selectedIndex);
            
            int option;
            option = JOptionPane.showConfirmDialog(this, "Do you want to delete this student ?" );
            if(option == 0){
                StudentManagement.deleteStudent(std.getId());
                showStudent();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Enter Full Name to search");
        
        if(input != null && input.length()>0){
            studentList = StudentManagement.findByFullName(input);
            tableModel.setRowCount(0);
        studentList.forEach((student) -> {
            tableModel.addRow(new Object[]{
                tableModel.getRowCount() + 1, student.getFullname(), student.getGender(), student.getEmail(), student.getPhoneNumber()
            });
        });
        } else {
            showStudent();
        }
    }//GEN-LAST:event_btnFindActionPerformed

